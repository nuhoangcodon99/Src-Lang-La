    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        if(listgift.size() <= 0){
            infoBox("Vui lòng thêm item"); return;
        }
        String code = this.jComboBox4.getText();
        int count =  Integer.parseInt(this.jComboBox5.getText());

        String query = "INSERT INTO `giftcode` (`code`, `count`, `time_expire`, `char_id`, `list_item`) VALUES (?, ?, '2030-12-31 00:00:00', -1, ?)";

        try (Connection con = PKoolVN.getConnection();
             PreparedStatement pstmt = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS)) {
            // Thiết lập giá trị cho các tham số
            ObjectMapper mapper = DataCenter.gI().mapper;
            pstmt.setString(1, code);
            pstmt.setInt(2, count);
            pstmt.setString(3, mapper.writeValueAsString(listgift));
            // Thực thi câu lệnh
            pstmt.executeUpdate();
            infoBox("Tạo thành công code: "+code+" Count nhập: "+count);
            listgift.clear();
            if(item != null) item.strOptions = null;
            setItem();
            this.jLabel2.setText("<html></html>");
        } catch (SQLException | IOException e) {
            infoBox("Thất bại tên mã code này đã được tạo");
        }


    }

