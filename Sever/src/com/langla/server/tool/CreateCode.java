    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        expiry = Integer.parseInt(this.jTextField4.getText());
        isLock = this.jCheckBox1.isSelected();
        quanity =  Integer.parseInt(this.jTextField5.getText());
        vang = Integer.parseInt(this.jTextField6.getText());
        vang_khoa = Integer.parseInt(this.jTextField7.getText());
        bac = Integer.parseInt(this.jTextField8.getText());
        bac_khoa = Integer.parseInt(this.jTextField9.getText());
        exp = Long.parseLong(this.jTextField10.getText());

        GiftcodeTemplate gift = new GiftcodeTemplate();
        if(item != null){
            gift.item = item;
            if(expiry >= 1) gift.item.expiry = 86400000L*expiry;
            gift.item.amount = quanity;
            gift.item.isLock = isLock;
        }

        gift.bac = bac;
        gift.bacKhoa = bac_khoa;
        gift.vang = vang;
        gift.vangKhoa = vang_khoa;
        gift.exp = exp;
        listgift.add(gift);

        String s = "";
        for (int i= 0; i < listgift.size(); i++){
            GiftcodeTemplate giftcodeTemplate = listgift.get(i);
            if(item != null){
                Vector vec = Item.getTextVec(giftcodeTemplate.item);
                s += ""+giftcodeTemplate.item.getItemTemplate().name+" >>> <br>";

                for (int b = 0; b < vec.size(); b++) {
                    s += ((LangLa_gp) vec.get(b)).a;
                    s += "<br>";
                }
            } else {
                s += "Bạc: "+gift.bac+"<br>";
                s += "Bạc khóa: "+gift.bacKhoa+"<br>";
                s += "Vàng: "+gift.vang+"<br>";
                s += "Vàng khóa: "+gift.vangKhoa+"<br>";
                s += "Exp: "+gift.exp+"<br>";
            }
        }

        //   System.out.println(item.strOptions);
        this.jLabel2.setText("<html>" + s + "</html>");
    }

